package br.com.rhinosistemas.bean;

public class ResilienteBean {

	private String nomeConsumidor;
	private String produtoConsumido;

	public String getNomeConsumidor() {
		return nomeConsumidor;
	}

	public void setNomeConsumidor(String nomeConsumidor) {
		this.nomeConsumidor = nomeConsumidor;
	}

	public String getProdutoConsumido() {
		return produtoConsumido;
	}

	public void setProdutoConsumido(String produtoConsumido) {
		this.produtoConsumido = produtoConsumido;
	}

}
